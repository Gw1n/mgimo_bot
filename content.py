# ──────────────────────────────────────────────
# Правила исключения вариантов
# Если Q1=2 → убрать вариант 3 в Q2 и Q3
# Если Q1=1 и Q2=1 → убрать вариант 3 в Q3
# ──────────────────────────────────────────────
def get_available_buttons(question_index: int, history: str) -> list:
    all_buttons = QUESTIONS[question_index]["buttons"]
    if question_index == 0:
        return all_buttons

    answers = history.split("_") if history else []
    q1 = answers[0] if len(answers) >= 1 else None
    q2 = answers[1] if len(answers) >= 2 else None

    if question_index == 1:
        # Q1=2 → убрать вариант 3 из Q2
        if q1 == "2":
            return [b for b in all_buttons if b[1] != "3"]
        return all_buttons

    if question_index == 2:
        # Q1=2 → убрать вариант 3 из Q3
        if q1 == "2":
            return [b for b in all_buttons if b[1] != "3"]
        # Q1=1 и Q2=1 → убрать вариант 3 из Q3
        if q1 == "1" and q2 == "1":
            return [b for b in all_buttons if b[1] != "3"]
        return all_buttons

    return all_buttons

