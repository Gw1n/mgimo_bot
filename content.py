# Индексы вопросов, которые влияют на результат
SCORING_INDICES = [i for i, q in enumerate(QUESTIONS) if q["scores"]]

# ──────────────────────────────────────────────
# Правила исключения вариантов
# Если scoring-Q1=2 → убрать вариант 3 в scoring-Q2 и scoring-Q3
# Если scoring-Q1=1 и scoring-Q2=1 → убрать вариант 3 в scoring-Q3
# history содержит ТОЛЬКО ответы на scoring-вопросы
# ──────────────────────────────────────────────
def get_available_buttons(question_index: int, history: str) -> list:
    """Возвращает отфильтрованный список кнопок для вопроса.

    history — строка scoring-ответов (например "1_2"), filler-ответы не входят.
    """
    all_buttons = QUESTIONS[question_index]["buttons"]

    # Filler-вопросы и первый scoring-вопрос — без фильтрации
    if not QUESTIONS[question_index]["scores"]:
        return all_buttons

    answers = history.split("_") if history else []
    q1 = answers[0] if len(answers) >= 1 else None
    q2 = answers[1] if len(answers) >= 2 else None

    # Определяем порядковый номер этого scoring-вопроса (0, 1, 2)
    scoring_order = SCORING_INDICES.index(question_index)

    if scoring_order == 0:
        return all_buttons

    if scoring_order == 1:
        # scoring-Q1=2 → убрать вариант 3 из scoring-Q2
        if q1 == "2":
            return [b for b in all_buttons if b[1] != "3"]
        return all_buttons

    if scoring_order == 2:
        # scoring-Q1=2 → убрать вариант 3 из scoring-Q3
        if q1 == "2":
            return [b for b in all_buttons if b[1] != "3"]
        # scoring-Q1=1 и scoring-Q2=1 → убрать вариант 3 из scoring-Q3
        if q1 == "1" and q2 == "1":
            return [b for b in all_buttons if b[1] != "3"]
        return all_buttons

    return all_buttons

